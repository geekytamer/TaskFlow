interface I18nContextValue {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  isRtl: boolean;
  t: (key: string, fallback?: string) => string;
}

const I18nContext = React.createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = React.useState<AppLanguage>('en');

  React.useEffect(() => {
    try {
      const stored = localStorage.getItem(LANGUAGE_KEY) as AppLanguage | null;
      if (stored === 'en' || stored === 'ar') setLanguageState(stored);
    } catch {}
  }, []);

  React.useEffect(() => {
    if (typeof document === 'undefined') return;
    document.documentElement.lang = language;
    document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr';
  }, [language]);

  const setLanguage = React.useCallback((value: AppLanguage) => {
    setLanguageState(value);
    try { localStorage.setItem(LANGUAGE_KEY, value); } catch {}
  }, []);

  const t = React.useCallback(
    (key: string, fallback?: string) => {
      const fromLang = dictionaries[language]?.[key];
      if (fromLang) return fromLang;
      const fromEnglish = dictionaries.en[key];
      if (fromEnglish) return fromEnglish;
      return fallback ?? key;
    },
    [language],
  );

  const value = React.useMemo(
    () => ({
      language,
      setLanguage,
      isRtl: language === 'ar',
      t,
    }),
    [language, setLanguage, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = React.useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return context;
}
